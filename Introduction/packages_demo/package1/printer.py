

def printUpperCase(text):
    """
    Prints the given text in uppercase.
    
    Args:
        text (str): The text to print.
    """
    print(text.upper())

def printLowerCase(text):
    """
    Prints the given text in lowercase.
    
    Args:
        text (str): The text to print.
    """
    print(text.lower())

def printTitleCase(text):
    """
    Prints the given text in title case.
    
    Args:
        text (str): The text to print.
    """
    print(text.title())